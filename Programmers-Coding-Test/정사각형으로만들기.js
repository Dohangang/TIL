function solution(arr) {
  let rows = arr.length;
  let cols = arr[0].length;

  if (rows > cols) {
    for (let i = 0; i < rows; i++) {
      while (arr[i].length < rows) {
        arr[i].push(0);
      }
    }
  }

  if (cols > rows) {
    while (arr.length < cols) {
      arr.push(new Array(cols).fill(0));
    }
  }

  return arr;
}
