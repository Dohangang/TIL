function solution(d, budget) {
  let sortedD = d.sort((a, b) => a - b);
  let answer = 0;
  let price = 0;

  for (let i = 0; i < sortedD.length; i++) {
    if (price + sortedD[i] <= budget) {
      price += sortedD[i];
      answer++;
    } else {
      break;
    }
  }

  return answer;
}
