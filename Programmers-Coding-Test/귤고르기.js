function solution(k, tangerine) {
  const count = {};
  tangerine.forEach((size) => (count[size] = (count[size] || 0) + 1));

  const sortedCounts = Object.values(count).sort((a, b) => b - a);

  let total = 0,
    types = 0;

  for (let i = 0; i < sortedCounts.length; i++) {
    const c = sortedCounts[i];
    total += c;
    types++;
    if (total >= k) {
      break;
    }
  }

  return types;
}

