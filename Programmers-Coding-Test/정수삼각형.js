function solution(triangle) {
  const n = triangle.length;

  const forMerging = triangle.map((row) => [...row]);

  for (let i = n - 2; i >= 0; i--) {
    for (let j = 0; j <= i; j++) {
      forMerging[i][j] += Math.max(
        forMerging[i + 1][j],
        forMerging[i + 1][j + 1]
      );
    }
  }

  return forMerging[0][0];
}

