function solution(cacheSize, cities) {
  if (cacheSize === 0) return cities.length * 5;

  const cache = new Map();
  let totalTime = 0;

  for (let city of cities) {
    city = city.toLowerCase();

    if (cache.has(city)) {
      totalTime += 1;
      cache.delete(city);
      cache.set(city, true);
    } else {
      totalTime += 5;
      if (cache.size >= cacheSize) {
        const oldestKey = cache.keys().next().value;
        cache.delete(oldestKey);
      }
      cache.set(city, true);
    }
  }

  return totalTime;
}

