function solution(number, limit, power) {
  let totalWeight = 0;

  const countDivisor = (num) => {
    let count = 0;
    for (let i = 1; i * i <= num; i++) {
      if (num % i === 0) {
        count++;
        if (i !== num / i) count++;
      }
    }
    return count;
  };

  for (let i = 1; i <= number; i++) {
    const divisorCount = countDivisor(i);
    totalWeight += divisorCount > limit ? power : divisorCount;
  }

  return totalWeight;
}

