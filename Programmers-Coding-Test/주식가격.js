function solution(prices) {
  const n = prices.length;
  const answer = Array.from({ length: n }, (_, i) => n - 1 - i);
  const stack = [];
  for (let i = 0; i < n; i++) {
    while (stack.length > 0 && prices[stack[stack.length - 1]] > prices[i]) {
      const topIndex = stack.pop();
      answer[topIndex] = i - topIndex;
    }
    stack.push(i);
  }

  return answer;
}
