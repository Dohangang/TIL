function solution(s) {
  let countZero = 0;
  let transNumber = 0;
  let memo = s;

  while (memo !== "1") {
    countZero += memo.split("0").length - 1;

    memo = memo.split("0").join("").length.toString(2);

    transNumber++;
  }

  return [transNumber, countZero];
}

