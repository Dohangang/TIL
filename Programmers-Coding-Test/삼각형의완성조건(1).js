function solution(sides) {
  // if(sides%1!==0 && sides!==Math.floor(sides)){return 'not natural number';}
  if (sides.length !== 3) {
    return "length error";
  }
  if (sides.find((x) => x < 1 || 1000 < x)) {
    return "sides range error";
  }

  if (
    2 * Math.max(sides[0], sides[1], sides[2]) <
    sides[0] + sides[1] + sides[2]
  ) {
    return 1;
  } else {
    return 2;
  }
}

