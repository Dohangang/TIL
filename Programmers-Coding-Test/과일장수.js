function solution(k, m, score) {
  score.sort((a, b) => b - a);

  let totalProfit = 0;

  for (let i = 0; i < Math.floor(score.length / m); i++) {
    let box = score.slice(i * m, (i + 1) * m);
    let minScore = box[m - 1];
    totalProfit += minScore * m;
  }

  return totalProfit;
}

