SELECT
  C.CLUB_ID,
  C.CLUB_NAME,
  COUNT(M.MEMBER_ID) AS NEW_MEMBER_COUNT
FROM
  CLUBS AS C
JOIN 
  CLUB_MEMBERS AS M ON C.CLUB_ID = M.CLUB_ID
WHERE
  C.CATEGORY = 'IT' 
  AND M.JOIN_DATE >= '2023-01-01'
GROUP BY
  C.CLUB_ID, C.CLUB_NAME
HAVING
  COUNT(M.MEMBER_ID) >= 5
ORDER BY 
  NEW_MEMBER_COUNT DESC, C.CLUB_ID ASC;